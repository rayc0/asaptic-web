export function t(obj, lang, fallback = "en") {
  if (obj && Object.hasOwn(obj, lang) && obj[lang] !== undefined) return obj[lang];
  if (obj && Object.hasOwn(obj, fallback) && obj[fallback] !== undefined) return obj[fallback];
  return "";
}

export function label(key, lang, fallback = "en") {
  return ui[lang]?.[key] ?? ui[fallback]?.[key] ?? "";
}

export function esc(value) {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

export function escAttr(value) {
  return esc(value)
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

export function safeHttpsUrl(url) {
  try {
    const parsed = new URL(String(url ?? ""));
    return parsed.protocol === "https:" ? parsed.href : "";
  } catch {
    return "";
  }
}

export function localePrefix(locale) {
  return locale === "en" ? "" : `/${locale}`;
}

export function cleanStandardUrl({ site = "", locale = "en", slug }) {
  return `${site}${localePrefix(locale)}/standard/${slug}`;
}
